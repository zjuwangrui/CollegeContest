#ifndef __MODULE_EYE_DETECT_H
#define __MODULE_EYE_DETECT_H

#include <stdint.h>
#include <stdbool.h>

/*
 * ===========================================================================
 *  eye_detect —— 基于熵值的闭眼检测 (院赛拓展题 2)
 * ===========================================================================
 *
 *  基本假设:
 *    JPG2COM 已开启人脸检测锁眼模式, 抠图区就是眼睛附近的小块灰度图.
 *    睁眼时视野里有虹膜/瞳孔 (黑) + 巩膜 (白) 高对比 → 熵值高;
 *    闭眼时视野被眼皮/皮肤覆盖 → 熵值低.
 *
 *  状态机 (含迟滞 + 时间过滤):
 *
 *       ┌──────────────┐
 *       │ OPEN         │◄──────────────────────────┐
 *       └──────┬───────┘                            │
 *              │ H < H_ENTER                        │
 *              ▼                                    │
 *       ┌──────────────┐   H ≥ H_EXIT              │
 *       │ MAYBE_CLOSED │──────────────────► 眨眼过滤, 回 OPEN
 *       └──────┬───────┘                            │
 *              │ 持续 T_CLOSE_MS 时长               │
 *              ▼                                    │
 *       ┌──────────────┐                            │
 *       │ CLOSED       │ H ≥ H_EXIT 稳定 T_OPEN_MS  │
 *       │ (触发提示)   │────────────────────────────┘
 *       └──────────────┘
 *
 *  参数在 eye_detect.c 顶部, 全部 #define, 需要调时改那里.
 * ===========================================================================
 */

/* 复位所有状态到 OPEN. 在 image_view_init 里调用一次. */
void eye_detect_init(void);

/* 每收到一帧新的熵值就调一次.
 *   H       : 当前帧绝对熵 (bit, 0..8)
 *   tick_ms : HAL_GetTick(), 用来做时间过滤. */
void eye_detect_feed(float H, uint32_t tick_ms);

/* 查询当前是否处于确认闭眼状态. */
bool eye_detect_is_closed(void);

#endif /* __MODULE_EYE_DETECT_H */
